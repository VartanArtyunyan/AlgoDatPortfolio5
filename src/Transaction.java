public class Transaction {
	int timestamp;
	double amount;
	String from;
	String to;
	
	
	public Transaction(int timestamp, double amount, String from, String to) {
		super();
		this.timestamp = timestamp;
		this.amount = amount;
		this.from = from;
		this.to = to;
	}
	
	

}
